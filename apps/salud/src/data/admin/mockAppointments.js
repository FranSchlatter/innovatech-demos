// Helper functions
export const getTodayAppointments = () => {
  const todayStr = formatDate(today)
  return mockAppointments.filter(apt => apt.date === todayStr)
}

export const getPendingAppointments = () => {
  return mockAppointments.filter(apt => apt.status === 'scheduled' || apt.status === 'confirmed')
}

export const getAppointmentsByDoctor = (doctorId) => {
  return mockAppointments.filter(apt => apt.doctorId === doctorId)
}

export const getAppointmentsByStatus = (status) => {
  return mockAppointments.filter(apt => apt.status === status)
}

export const getUpcomingAppointments = () => {
  const todayStr = formatDate(today)
  return mockAppointments.filter(apt => apt.date >= todayStr && apt.status !== 'cancelled' && apt.status !== 'completed')
}
