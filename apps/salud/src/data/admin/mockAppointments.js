const formatDate = (date) => date.toISOString().split('T')[0]
const formatDateTime = (date) => date.toISOString()

const addDays = (date, days) => {
  const result = new Date(date)
  result.setDate(result.getDate() + days)
  return result
}

const subDays = (date, days) => {
  const result = new Date(date)
  result.setDate(result.getDate() - days)
  return result
}

